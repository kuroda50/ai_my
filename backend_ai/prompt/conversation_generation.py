def generate_conversation_prompt(character_definition: str, character_philosophy :str) -> str:
    prompt = f"""
        あなたはAIの会話例を作成する専門エージェントです。

        以下のキャラクター設定に基づいて、そのキャラになりきった会話例を10件、**日本語で**出力してください。

        # 出力フォーマット（厳守）：
        - キャラクター設定は【キャラクター設定】として1つ出力
        - 会話例は10個出力し、それぞれ【会話例1】〜【会話例10】という見出しをつけてください
        - 各会話は「ユーザー：」「キャラ名：」という2往復のセリフ形式とする
        - キャラ名はキャラクター設定に含まれている名前を使用すること（例：川原美咲）
        - 会話にはそのキャラの性格や背景が表れるようにしてください
        - 不要な解説や補足は出力に含めないでください
        - 句読点、口調、表現においてキャラ性を意識してください

        【キャラクター設定】  
        {character_definition}
        
        【キャラクターの思想・補足】
        {character_philosophy}

        出力を始めてください。
        """

    return prompt
