import json

def  generate_character_prompt(user_answers: json) -> str:
    prompt = f"""
    あなたは、ユーザーの悩み・コンプレックスをもとに「共感できるキャラクター」を生成するAIです。

    ユーザーは以下の質問に答えています。その内容をもとに、
    「ユーザーとできるだけ近い境遇・悩み・価値観を持つキャラクター」を1人設計してください。

    ---

    **キャラ設計における重要なルール：**

    - キャラクターは、ユーザーと似た性格・悩み・日常の癖を持つようにしてください。
    - 見た目・生活環境・発言の温度感も、ユーザーが「これは自分の分身かも」と思えるようなリアリティを持たせてください。
    - キャラは完璧な存在である必要はありません。むしろ、欠けたところ・苦しんでいる点を素直に描いてください。
    - **キャラクターの性別は「女性」固定**です。
    - **キャラクターはアニメやゲーム風の設定ではなく、現実世界にいそうな人物像で設計**してください。
    - キャラの変化（成長）を物語るための「人との出会い」や「小さなきっかけ」も描いてください。

    ---

    【質問へのユーザーの回答】

    0. あなたの年齢、性別、現在の立場（例：大学生・社会人・主婦など）を教えてください
    → {user_answers["q0_answer"]}

    1. あなたが今抱えている「自分の中で一番重い悩み」や「ずっと抱えているコンプレックス」は何ですか？
    → {user_answers["q1_answer"]}

    2. その悩みやコンプレックスによって、日常で困ることや行動が制限される場面はありますか？
    → {user_answers["q2_answer"]}

    3. それに向き合おうとしたことはありますか？ ある場合は、うまくいかなかった・挫折した理由も教えてください。
    → {user_answers["q3_answer"]}

    4. その悩みを持っているあなた自身を、もし「別の誰か」だとして見たとき、どんな人物だと感じますか？
    → {user_answers["q4_answer"]}

    5. その人（＝あなたと同じ悩みを持つキャラ）が、少しずつでも前に進むために必要な出来事や人との出会いがあるとすれば、どんなものだと思いますか？
    → {user_answers["q5_answer"]}

    ---

    【出力例のフォーマット】

    ### キャラクターシート

    - **名前**：  
    - **性別・年齢**：  
    - **外見・服装**：  
    - **一人称・口調**：  
    - **性格・価値観**：  
    - **背景・現在の状況**：  
    - **悩み・コンプレックス**：  
    - **目標・モチベーション**：  
    - **話し方・会話スタイル**：  
    - **他者との関係性（理想像）**：  
    - **このキャラが物語で少しずつ変わっていくきっかけ**：

    ---

    """
    return prompt